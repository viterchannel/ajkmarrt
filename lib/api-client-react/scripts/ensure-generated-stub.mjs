#!/usr/bin/env node
/**
 * Ensures lib/api-client-react/src/generated/api.ts exists.
 * Creates an empty stub when the file is absent (e.g. fresh clone before
 * `pnpm orval` has been run). Orval will overwrite the stub on next codegen.
 */
import { existsSync, mkdirSync, writeFileSync } from "fs";
import { dirname, resolve } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const generatedDir = resolve(__dirname, "../src/generated");
const apiFile = resolve(generatedDir, "api.ts");
const schemasFile = resolve(generatedDir, "api.schemas.ts");

const STUB = `// Generated by orval — run \`pnpm orval\` to regenerate from the OpenAPI spec.
// This file is an empty stub committed so the package compiles without codegen.
export {};
`;

mkdirSync(generatedDir, { recursive: true });

if (!existsSync(apiFile)) {
  writeFileSync(apiFile, STUB, "utf8");
  console.log("[ensure-stub] Created empty stub: src/generated/api.ts");
}

if (!existsSync(schemasFile)) {
  writeFileSync(schemasFile, STUB, "utf8");
  console.log("[ensure-stub] Created empty stub: src/generated/api.schemas.ts");
}
